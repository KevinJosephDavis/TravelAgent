EXTRACT_CITY_PROMPT = """
    <Role>
    你是一个信息抽取助手。
    </Role>

    <Task>
    从用户的问题中提取城市名称。
    只输出城市名，不要解释，不要多余内容。
    没有城市则输出空字符串。
    如果用户输入的城市具体到某个区/县，则只需要返回区/县名。
    如果用户输入的城市仅仅是某个市，则只需要返回城市名。
    例如，用户输入的是“查询广州市番禺区的天气”或“查询广州番禺的天气”，你只需要提取出“番禺区”并返回。
    如果用户输入的是“查询广州的天气”，你只需要提取出“广州市”并返回。
    </Task>

    用户问题：{user_input}
    """

EXTRACT_ROUTE_PROMPT = """
    <Role>
    你是一个信息抽取助手。
    </Role>

    <Task>
    从用户的问题中提取路径规划所需的信息。
    你需要提取：起点、终点、出行方式。
    输出格式：起点|终点|出行方式（用竖线分隔，不要换行，不要多余内容）

    关于出行方式的判断：
    - 如果用户提到"开车"、"自驾"、"驾车"、"汽车"等，出行方式为"驾车"
    - 如果用户提到"步行"、"走路"、"走着去"等，出行方式为"步行"
    - 如果用户提到"骑行"、"骑车"、"自行车"、"单车"等，出行方式为"骑行"
    - 如果用户提到"公交"、"地铁"、"公共交通"等，出行方式为"公交"
    - 如果用户没有明确说明，默认为"驾车"

    示例：
    用户输入："帮我规划一下从北京到上海的驾车路线"
    输出：北京|上海|驾车

    用户输入："我想从广州步行去深圳"
    输出：广州|深圳|步行

    用户输入："从成都骑行到重庆要多久"
    输出：成都|重庆|骑行

    如果无法提取起点或终点，对应位置输出空字符串。
    </Task>

    用户问题：{user_input}
    """